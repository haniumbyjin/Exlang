package kr.ac.kopo.model;

public class MeetingMember {
	String mmId;
	Long mCode;
	int mmCode;
	
	public String getMmId() {
		return mmId;
	}
	public void setMmId(String mmId) {
		this.mmId = mmId;
	}
	public Long getmCode() {
		return mCode;
	}
	public void setmCode(Long mCode) {
		this.mCode = mCode;
	}
	public int getMmCode() {
		return mmCode;
	}
	public void setMmCode(int mmCode) {
		this.mmCode = mmCode;
	}
}
